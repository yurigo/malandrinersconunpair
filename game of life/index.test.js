describe('Basic Neighbours', () => {
    test('Basic Neighbours #1', () => {
        const grid = [
            [1, 0],
            [0, 1],
        ]

        expect(numberOfAliveNeighbours(grid, 0, 0)).toBe(1)
        expect(numberOfAliveNeighbours(grid, 0, 1)).toBe(2)
        expect(numberOfAliveNeighbours(grid, 1, 0)).toBe(2)
        expect(numberOfAliveNeighbours(grid, 1, 1)).toBe(1)
    })

    test('Basic Neighbours #2', () => {
        const grid = [
            [1, 1],
            [1, 1],
        ]

        expect(numberOfAliveNeighbours(grid, 0, 0)).toBe(3)
        expect(numberOfAliveNeighbours(grid, 0, 1)).toBe(3)
        expect(numberOfAliveNeighbours(grid, 1, 0)).toBe(3)
        expect(numberOfAliveNeighbours(grid, 1, 1)).toBe(3)
    })

    test('Basic Neighbours #3', () => {
        const grid = [
            [1, 1, 1],
            [1, 1, 0],
        ]

        expect(numberOfAliveNeighbours(grid, 0, 0)).toBe(3)
        expect(numberOfAliveNeighbours(grid, 0, 1)).toBe(4)
        expect(numberOfAliveNeighbours(grid, 0, 2)).toBe(2)
        expect(numberOfAliveNeighbours(grid, 1, 0)).toBe(3)
        expect(numberOfAliveNeighbours(grid, 1, 1)).toBe(4)
        expect(numberOfAliveNeighbours(grid, 1, 2)).toBe(3)
    })
});







